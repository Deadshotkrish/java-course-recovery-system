    // load users (read from file then load to jtable)
    private void loadUsers() {
        try {
            List<User> allUsers = admin.getUserDAO().loadAllUsers();
            tableModel = new UserTableModel(allUsers);
            tblUsers.setModel(tableModel);
            sorter = new TableRowSorter<UserTableModel>(tableModel);
            tblUsers.setRowSorter(sorter);
        } catch (IOException e) {
            JOptionPane.showMessageDialog(this, "Failed to load users: " + e.getMessage());
        }
    }

    // validate username, email, and password format
    private boolean isPasswordValid(String pwd) { // min. 6 char with digit, max 15
        if (pwd.length() < 6 || pwd.length() > 15) return false;
        
        boolean hasLetter = false, hasDigit = false;
        for (char c : pwd.toCharArray()) {
            if (Character.isLetter(c)) hasLetter = true;
            if (Character.isDigit(c)) hasDigit = true;
        }
        return hasLetter && hasDigit;
    }
   
    private boolean isUsernameValid(String username) { // min 3 char, max 18 char, can include underscore
        if (username.length() < 3 || username.length() > 18) return false;

        for (char c : username.toCharArray()) {
            if (!Character.isLetterOrDigit(c) && c != '_') {
                // invalid character found
                return false;
            }
        }
        return true;
    }
    
    private boolean isEmailValid(String email) {
        if (email == null) return false;
        email = email.trim();

        if (email.isEmpty()) return false;
        if (email.length() > 100) return false;

        // format is no spaces, one '@', at least one dot in domain, tld ≥ 2 letters (support .com, .ru, etc)
        return email.matches("^[^@\\s]+@[^@\\s]+\\.[A-Za-z]{2,}$");
    }

    
    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // TODO add your handling code here:
        JTextField txtUser = new JTextField();
        JTextField txtPass = new JTextField();
        JTextField txtEmail = new JTextField();   
        JComboBox<String> cbRole = new JComboBox<>(new String[]{"SystemAdmin", "AcademicOfficer"});
        JCheckBox chkActive = new JCheckBox("Active", true);

        Object[] form = {
            "Username:", txtUser,
            "Email:", txtEmail,
            "Password:", txtPass,
            "Role:", cbRole,
            chkActive
        };

        int result = JOptionPane.showConfirmDialog(this, form, "Add User", JOptionPane.OK_CANCEL_OPTION);
        if (result != JOptionPane.OK_OPTION) return;

        String username = txtUser.getText().trim();
        String password = txtPass.getText().trim();
        String email = txtEmail.getText().trim();
        String role = (String) cbRole.getSelectedItem();
        boolean active = chkActive.isSelected();

        if (username.isEmpty() || password.isEmpty() || email.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Username, password and/or email cannot be empty.");
            return;
        }
        
        if (!isUsernameValid(username)) {
            JOptionPane.showMessageDialog(this, "Username must be 3–18 characters long (letters, digits, or underscores only).");
            return;
        }


        if (!isPasswordValid(password)) {
            JOptionPane.showMessageDialog(this, "Password must be 6–15 characters long and contain at least one letter and one digit.");
            return;
        }
        
        if (!isEmailValid(email)) {
            JOptionPane.showMessageDialog(this,
                "Please enter a valid email (e.g. name@example.com), max 100 characters.");
            return;
        }

        try {
            List<User> existing = admin.getUserDAO().loadAllUsers();
            boolean duplicate = existing.stream().anyMatch(u -> u.getUsername().equalsIgnoreCase(username)); // username duplicate check
            if (duplicate) {
                JOptionPane.showMessageDialog(this, "Username already exists. Please choose an available name.");
                return;
            }

            String newId = admin.getUserDAO().nextUserId();
            
            User newUser;
            if ("SystemAdmin".equalsIgnoreCase(role)) {
                newUser = new SystemAdmin(newId, username, password, active, email);
            } else {
                newUser = new AcademicOfficer(newId, username, password, active, email);
            }
            
            admin.addUser(newUser);
            loadUsers();
            JOptionPane.showMessageDialog(this, "User added successfully.");

            //
            // Email part
            //
            try 
            {
                NotificationService.sendAccountCreatedEmail(newUser, password);
                JOptionPane.showMessageDialog(this, "Account creation email has been sent.");
            } 
            catch (Exception e){}

        } catch (IOException e) {
            JOptionPane.showMessageDialog(this, "Error adding user: " + e.getMessage());
        }
    }//GEN-LAST:event_btnAddActionPerformed

    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
        // TODO add your handling code here:
        int row = tblUsers.getSelectedRow();
        if (row < 0) {
            JOptionPane.showMessageDialog(this, "Please select a user to edit.");
            return;
        }

        User selected = tableModel.getUserAt(row);
        boolean isOwnAccount = selected.getUserId().equals(admin.getUserId());
        
        JTextField txtUser = new JTextField(selected.getUsername());
        JTextField txtPass = new JTextField();
        JComboBox<String> cbRole = new JComboBox<>(new String[]{"SystemAdmin", "AcademicOfficer"});
        cbRole.setSelectedItem(selected.getRole());
        JCheckBox chkActive = new JCheckBox("Active", selected.isActive());
        JTextField txtEmail = new JTextField(selected.getEmail() != null ? selected.getEmail() : "");

        
        // Disable role and active controls if editing own account to prevent lock-out
        if (isOwnAccount) {
            cbRole.setEnabled(false);
            chkActive.setEnabled(false);
        }
        
        Object[] form = {
            "Username:", txtUser,
            "Email:", txtEmail,
            "New Password (leave blank to keep):", txtPass,
            "Role:", cbRole,
            chkActive
        };
        
        if (isOwnAccount) {
            form = new Object[] {
                "Username:", txtUser,
                "Email:", txtEmail,
                "New Password (leave blank to keep):", txtPass,
                "Role:", cbRole,
                chkActive,
                new JLabel(" "),
                new JLabel("Note: You cannot change your own role or active status.")
            };
        }

        int result = JOptionPane.showConfirmDialog(this, form, "Edit User", JOptionPane.OK_CANCEL_OPTION);
        if (result != JOptionPane.OK_OPTION) return;

        String username = txtUser.getText().trim();
        String password = txtPass.getText().trim();
        String email = txtEmail.getText().trim();
        String role = (String) cbRole.getSelectedItem();
        boolean active = chkActive.isSelected();
        
        if (username.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Username cannot be empty.");
            return;
        }
  
        if (!isUsernameValid(username)) {
            JOptionPane.showMessageDialog(this, "Username must be 3–18 characters long (letters, digits, or underscores only).");
            return;
        }
        
        if (!password.isEmpty() && !isPasswordValid(password)) {
            JOptionPane.showMessageDialog(this, "Password must be 6–15 characters long and contain at least one letter and one digit.");
            return;
        }
        
        if (!isEmailValid(email)) {
            JOptionPane.showMessageDialog(this,
                "Please enter a valid email (e.g. name@example.com), max 100 characters.");
            return;
        }

        try {
            List<User> existing = admin.getUserDAO().loadAllUsers();
            boolean duplicate = existing.stream()
                    .anyMatch(u -> u.getUsername().equalsIgnoreCase(username)
                            && !u.getUserId().equals(selected.getUserId())); // check for duplicate AND exclude edited username

            if (duplicate) {
                JOptionPane.showMessageDialog(this, "Username already exists. Please choose an available name.");
                return;
            }
            
            selected.setUsername(username);
            selected.setEmail(email);   
            if (!password.isEmpty()) selected.setPassword(password);
            
            // Only update role and active status if not editing own account
            if (!isOwnAccount) {
                selected.setRole(role);
                selected.setActive(active);
            }

            admin.updateUser(selected);
            loadUsers();
            JOptionPane.showMessageDialog(this, "User updated successfully.");

            //
            // Email part
            //
            try 
            {
                edit ++;
                NotificationService.sendAccountUpdatedEmail(selected, edit);
                JOptionPane.showMessageDialog(this, "Account update email has been sent.");
            } 
            catch (Exception e){}

            
        } catch (IOException e) {
            JOptionPane.showMessageDialog(this, "Error updating user: " + e.getMessage());
        }
    }//GEN-LAST:event_btnEditActionPerformed

    private void btnDeactivateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeactivateActionPerformed
        // TODO add your handling code here:
        int row = tblUsers.getSelectedRow();
        if (row < 0) {
            JOptionPane.showMessageDialog(this, "Please select a user to deactivate.");
            return;
        }

        User selected = tableModel.getUserAt(row);
        
        if (selected.getUserId().equals(admin.getUserId())) {
            JOptionPane.showMessageDialog(this,
                "Account is currently active. You cannot deactivate your own account.",
                "Action Not Allowed",
                JOptionPane.WARNING_MESSAGE);
            return;
        }
            
        if (!selected.isActive()) {
            JOptionPane.showMessageDialog(this,
                "This user is already deactivated.",
                "Action Not Allowed",
                JOptionPane.INFORMATION_MESSAGE);
            return;
        }
        
        int confirm = JOptionPane.showConfirmDialog(this,
                "Deactivate user " + selected.getUsername() + "?",
                "Confirm", JOptionPane.YES_NO_OPTION);
        if (confirm != JOptionPane.YES_OPTION) return;

        try {
            admin.removeUser(selected.getUserId());
            loadUsers();
            JOptionPane.showMessageDialog(this, "User deactivated.");
        } catch (IOException e) {
            JOptionPane.showMessageDialog(this, "Error deactivating user: " + e.getMessage());
        }
    }//GEN-LAST:event_btnDeactivateActionPerformed


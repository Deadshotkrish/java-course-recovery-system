    private static class UserTableModel extends AbstractTableModel {
        private final String[] cols = {"ID", "Username", "Email", "Role", "Active"};
        private List<User> users;
        public UserTableModel(List<User> users) { this.users = users; }
        public void setUsers(List<User> users) { this.users = users; fireTableDataChanged(); }
        public User getUserAt(int row) { return users.get(row); }

        @Override public int getRowCount() { return users == null ? 0 : users.size(); }
        @Override public int getColumnCount() { return cols.length; }
        @Override public String getColumnName(int c) { return cols[c]; }
        @Override public Object getValueAt(int r, int c) {
            User u = users.get(r);
            return switch (c) {
                case 0 -> u.getUserId();
                case 1 -> u.getUsername();
                case 2 -> u.getEmail();
                case 3 -> u.getRole();
                case 4 -> u.isActive() ? "Yes" : "No";
                default -> "";
            };
        }
    }

